function openmenu() {
  document.getElementById("side-menu").style.display="block";
  document.getElementById("start-menu-btn").style.display="none";
  document.getElementById("close-menu-btn").style.display="block";

}
function closemenu() {
  document.getElementById("side-menu").style.display="none";
  document.getElementById("start-menu-btn").style.display="block";
  document.getElementById("close-menu-btn").style.display="none";

}
